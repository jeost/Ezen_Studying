package Day08;

public class Day08_4 { // c s

	// p. 267~268 : Setter , Getter 메소드
		
	public static void main(String[] args) { // m s
		
		Car myCar = new Car();
		
		//잘못된 속도 변경
		myCar.setSpeed(-50);
		System.out.println("현재 속도 : "+ myCar.getSpeed());
		
		//올바른 속도 변경
		myCar.setSpeed(60);
		
		//멈춤
		if(!myCar.isStop()) {
			myCar.setStop(true);
		} 
		System.out.println("현재 속도 : "+ myCar.getSpeed());
	} // m e
} // c e
